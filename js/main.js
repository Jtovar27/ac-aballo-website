function applyLanguage(lang) {
  var t = translations[lang] || translations.es;

  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      el.textContent = t[key];
    }
  });

  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) {
      el.innerHTML = t[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-placeholder');
    if (t[key] !== undefined) {
      el.setAttribute('placeholder', t[key]);
    }
  });

  document.documentElement.lang = lang;
  localStorage.setItem('lang', lang);
}

var contactForm = document.getElementById('contact-form');
if (contactForm) {
  contactForm.addEventListener('submit', function (e) {
    e.preventDefault();

    var btn = document.getElementById('submit-btn');
    var successMsg = document.getElementById('form-success');
    var errorMsg = document.getElementById('form-error');
    var lang = localStorage.getItem('lang') || 'es';
    var t = translations[lang] || translations.es;
    var btnSpan = btn ? btn.querySelector('span') : null;

    if (btn) btn.disabled = true;
    if (btnSpan) btnSpan.textContent = t['contact.form.sending'];

    fetch('https://api.web3forms.com/submit', {
      method: 'POST',
      body: new FormData(contactForm)
    })
      .then(function (res) { return res.json(); })
      .then(function (data) {
        if (data.success) {
          if (successMsg) successMsg.classList.remove('hidden');
          if (errorMsg) errorMsg.classList.add('hidden');
          contactForm.reset();
        } else {
          if (errorMsg) errorMsg.classList.remove('hidden');
          if (successMsg) successMsg.classList.add('hidden');
        }
      })
      .catch(function () {
        if (errorMsg) errorMsg.classList.remove('hidden');
        if (successMsg) successMsg.classList.add('hidden');
      })
      .finally(function () {
        if (btn) btn.disabled = false;
        if (btnSpan) {
          btnSpan.textContent = translations[localStorage.getItem('lang') || 'es']['contact.form.submit'];
        }
      });
  });
}

function setupLangToggle(btnId, labelId) {
  var btn = document.getElementById(btnId);
  var label = document.getElementById(labelId);

  if (!btn) return;

  function refresh(lang) {
    if (label) {
      label.textContent = lang === 'es' ? 'EN' : 'ES';
    }
  }

  refresh(localStorage.getItem('lang') || 'es');

  btn.addEventListener('click', function () {
    var current = localStorage.getItem('lang') || 'es';
    var next = current === 'es' ? 'en' : 'es';

    applyLanguage(next);
    refresh(next);

    var otherLabel = document.getElementById(
      btnId === 'lang-toggle' ? 'lang-label-mobile' : 'lang-label'
    );

    if (otherLabel) {
      otherLabel.textContent = next === 'es' ? 'EN' : 'ES';
    }
  });
}

