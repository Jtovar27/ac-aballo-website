function applyLanguage(lang) {
  var t = translations[lang] || translations['es'];
  // Plain text elements
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    var key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) el.textContent = t[key];
  });
  // HTML elements (contain tags like <br>, <em>)
  document.querySelectorAll('[data-i18n-html]').forEach(function (el) {
    var key = el.getAttribute('data-i18n-html');
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  document.documentElement.lang = lang;
  localStorage.setItem('lang', lang);
}

// Mobile nav toggle
var menuToggle = document.getElementById('menu-toggle');
var mobileMenu = document.getElementById('mobile-menu');

if (menuToggle && mobileMenu) {
  menuToggle.addEventListener('click', function () {
    mobileMenu.classList.toggle('hidden');
  });

  // Auto-close mobile menu when a nav link is clicked
  mobileMenu.querySelectorAll('a').forEach(function (link) {
    link.addEventListener('click', function () {
      mobileMenu.classList.add('hidden');
    });
  });
}

// Web3Forms contact form
var contactForm = document.getElementById('contact-form');
if (contactForm) {
  contactForm.addEventListener('submit', function (e) {
    e.preventDefault();
    var btn = document.getElementById('submit-btn');
    var successMsg = document.getElementById('form-success');
    var errorMsg = document.getElementById('form-error');
    btn.disabled = true;
    btn.textContent = 'Enviando...';
    fetch('https://api.web3forms.com/submit', {
      method: 'POST',
      body: new FormData(contactForm)
    })
      .then(function (res) { return res.json(); })
      .then(function (data) {
        if (data.success) {
          successMsg.classList.remove('hidden');
          errorMsg.classList.add('hidden');
          contactForm.reset();
        } else {
          errorMsg.classList.remove('hidden');
          successMsg.classList.add('hidden');
        }
      })
      .catch(function () {
        errorMsg.classList.remove('hidden');
        successMsg.classList.add('hidden');
      })
      .finally(function () {
        btn.disabled = false;
        btn.innerHTML = 'Enviar Mensaje <svg xmlns="http://www.w3.org/2000/svg" width="15" height="15" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" viewBox="0 0 24 24"><path d="M22 2L11 13M22 2l-7 20-4-9-9-4 20-7z"/></svg>';
      });
  });
}

// ── Language toggle buttons ──
function setupLangToggle(btnId, labelId) {
  var btn = document.getElementById(btnId);
  var label = document.getElementById(labelId);
  if (!btn) return;
  function refresh(lang) {
    if (label) label.textContent = lang === 'es' ? 'EN' : 'ES';
  }
  refresh(localStorage.getItem('lang') || 'es');
  btn.addEventListener('click', function () {
    var next = (localStorage.getItem('lang') || 'es') === 'es' ? 'en' : 'es';
    applyLanguage(next);
    refresh(next);
    var otherLabel = document.getElementById(btnId === 'lang-toggle' ? 'lang-label-mobile' : 'lang-label');
    if (otherLabel) otherLabel.textContent = next === 'es' ? 'EN' : 'ES';
  });
}
setupLangToggle('lang-toggle', 'lang-label');
setupLangToggle('lang-toggle-mobile', 'lang-label-mobile');

